import torch
import torch.nn as nn
from torch.nn import functional as F
import wandb


from datasets import load_dataset
from transformers import AutoTokenizer

import time

dataset = load_dataset(
    "wikitext",
    "wikitext-2-raw-v1"
)

print(dataset)

print(dataset["train"][0])

tokenizer = AutoTokenizer.from_pretrained("gpt2")

print(tokenizer("hello world"))

def tokenize_function(example):

    return tokenizer(
        example["text"],
        truncation=True,
        max_length=1024
    )

tokenized_dataset = dataset.map(
    tokenize_function,
    batched=True,
    remove_columns=["text"]
)

# -----------------------------
# Convert dataset into token stream
# -----------------------------

train_ids = []

for example in tokenized_dataset["train"]:

    train_ids.extend(example["input_ids"])

train_ids = torch.tensor(train_ids)
# -----------------------------
# Batch settings
# -----------------------------

batch_size = 1
seq_len = 1024

# -----------------------------
# Batch function
# -----------------------------

def get_batch():

    ix = torch.randint(
        0,
        len(train_ids) - seq_len - 1,
        (batch_size,)
    )

    x = torch.stack([
        train_ids[i:i+seq_len]
        for i in ix
    ])

    y = torch.stack([
        train_ids[i+1:i+seq_len+1]
        for i in ix
    ])

    return x, y

# -----------------------------
# Attention Head
# -----------------------------

class Head(nn.Module):

    def __init__(self, d_model, head_size):

        super().__init__()

        self.key = nn.Linear(d_model, head_size, bias=False)

        self.query = nn.Linear(d_model, head_size, bias=False)

        self.value = nn.Linear(d_model, head_size, bias=False)

        self.register_buffer(
            'mask',
            torch.tril(torch.ones(1024, 1024))
        )

    def forward(self, x):

        B, T, C = x.shape

        k = self.key(x)

        q = self.query(x)

        v = self.value(x)

        scores = q @ k.transpose(-2, -1) / (C ** 0.5)

        scores = scores.masked_fill(
            self.mask[:T, :T] == 0,
            float('-inf')
        )

        weights = F.softmax(scores, dim=-1)

        out = weights @ v

        return out

# -----------------------------
# Multi Head Attention
# -----------------------------

class MultiHeadAttention(nn.Module):

    def __init__(self, d_model, num_heads):

        super().__init__()

        head_size = d_model // num_heads

        self.heads = nn.ModuleList([
            Head(d_model, head_size)
            for _ in range(num_heads)
        ])

        self.proj = nn.Linear(d_model, d_model)

    def forward(self, x):

        out = torch.cat(
            [h(x) for h in self.heads],
            dim=-1
        )

        out = self.proj(out)

        return out

# -----------------------------
# FeedForward
# -----------------------------

class FeedForward(nn.Module):

    def __init__(self, d_model):

        super().__init__()

        self.net = nn.Sequential(
            nn.Linear(d_model, 4 * d_model),
            nn.ReLU(),
            nn.Linear(4 * d_model, d_model)
        )

    def forward(self, x):

        return self.net(x)

# -----------------------------
# Transformer Block
# -----------------------------

class Block(nn.Module):

    def __init__(self, d_model, num_heads):

        super().__init__()

        self.attn = MultiHeadAttention(
            d_model,
            num_heads
        )

        self.ffwd = FeedForward(d_model)

        self.ln1 = nn.LayerNorm(d_model)

        self.ln2 = nn.LayerNorm(d_model)

    def forward(self, x):

        x = x + self.attn(self.ln1(x))

        x = x + self.ffwd(self.ln2(x))

        return x

# -----------------------------
# GPT Model
# -----------------------------

class GPT(nn.Module):

    def __init__(
        self,
        vocab_size,
        d_model,
        num_heads,
        num_layers
    ):

        super().__init__()

        self.token_embedding = nn.Embedding(
            vocab_size,
            d_model
        )

        self.pos_embedding = nn.Embedding(
            1024,
            d_model
        )

        self.blocks = nn.Sequential(
            *[
                Block(d_model, num_heads)
                for _ in range(num_layers)
            ]
        )

        self.lm_head = nn.Linear(
            d_model,
            vocab_size
        )

    def forward(self, x):

        B, T = x.shape

        token_emb = self.token_embedding(x)

        pos_emb = self.pos_embedding(
            torch.arange(T, device=x.device)
        )

        x = token_emb + pos_emb

        x = self.blocks(x)

        logits = self.lm_head(x)

        return logits

# -----------------------------
# Device
# -----------------------------

device = "cuda" if torch.cuda.is_available() else "cpu"

# -----------------------------
# Model
# -----------------------------

model = GPT(
    vocab_size=tokenizer.vocab_size,
    d_model=128,
    num_heads=4,
    num_layers=4
).to(device)

# -----------------------------
# Optimizer + Scheduler
# -----------------------------

optimizer = torch.optim.AdamW(
    model.parameters(),
    lr=3e-4
)

scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(
    optimizer,
    T_max=200
)

# -----------------------------
# Parameter count
# -----------------------------

print(sum(p.numel() for p in model.parameters()))

# -----------------------------
# wandb
# -----------------------------
wandb.login()

wandb.init(
    project="mini-gpt-wikitext"
)
start_time = time.time()

# -----------------------------
# Training loop
# -----------------------------
model.train()

for step in range(200):

    x, y = get_batch()

    x = x.to(device)

    y = y.to(device)

    logits = model(x)

    B, T, C = logits.shape

    logits = logits.view(B * T, C)

    y = y.view(B * T)

    loss = F.cross_entropy(logits, y)

    ppl = torch.exp(loss)

    optimizer.zero_grad()

    loss.backward()

    optimizer.step()

    scheduler.step()

    wandb.log({
        "train_loss": loss.item(),
        "perplexity": ppl.item(),
        "learning_rate": scheduler.get_last_lr()[0]
    })

    if step % 10 == 0:

        elapsed = time.time() - start_time

        tokens_per_sec = (
          batch_size * seq_len * (step + 1)
        ) / elapsed

        print(
            f"Step {step} | "
            f"Loss: {loss.item():.4f} | "
            f"PPL: {ppl.item():.4f} | "
            f"Tokens/sec: {tokens_per_sec:.2f}"
        )

val_ids = []

for example in tokenized_dataset["validation"]:

    val_ids.extend(example["input_ids"])

val_ids = torch.tensor(val_ids)

def get_val_batch():

    ix = torch.randint(
        0,
        len(val_ids) - seq_len - 1,
        (batch_size,)
    )

    x = torch.stack([
        val_ids[i:i+seq_len]
        for i in ix
    ])

    y = torch.stack([
        val_ids[i+1:i+seq_len+1]
        for i in ix
    ])

    return x, y

model.eval()

with torch.no_grad():

    x, y = get_val_batch()

    x = x.to(device)

    y = y.to(device)

    logits = model(x)

    B, T, C = logits.shape

    logits = logits.view(B * T, C)

    y = y.view(B * T)

    val_loss = F.cross_entropy(logits, y)

    val_ppl = torch.exp(val_loss)

print("Validation Loss:", val_loss.item())

print("Validation Perplexity:", val_ppl.item())

model.train()

